def read(p):
    return p.read_text(encoding="utf-8")

def write(p, content):
    p.write_text(content, encoding="utf-8", newline="\n")
    print(f"  wrote {p.name} ({len(content)//1024}KB)")


# ─── CSS helpers ──────────────────────────────────────────────

def extract_style_block(content):
    """Return (before, css_inner, after) splitting on <style>…</style>."""
    m = re.search(r"(<style>)(.*?)(</style>)", content, re.DOTALL)
    if not m:
        return None, None, None
    return content[: m.start()], m.group(2), content[m.end() :]


def extract_root_block(css):
    """Pull :root { … } from css; return (root_str, remaining_css)."""
    start = css.find(":root")
    if start == -1:
        return "", css
    brace = css.find("{", start)
    depth, pos = 0, brace
    while pos < len(css):
        if css[pos] == "{":
            depth += 1
        elif css[pos] == "}":
            depth -= 1
            if depth == 0:
                root = css[start : pos + 1]
                rest = css[:start] + css[pos + 1 :]
                return root.strip(), rest
        pos += 1
    return "", css


# ─── TYPE A: topics 01-14 ────────────────────────────────────

def build_shared_a_css():
    """Build topic-shared-a.css from topic-01 (largest, most complete CSS)."""
    content = read(TOPICS_DIR / "topic-01.html")
    _, css, _ = extract_style_block(content)
    if css is None:
        raise RuntimeError("No <style> in topic-01.html")

    _, shared = extract_root_block(css)
    shared = shared.strip()

    # Ensure KaTeX helpers are present (some topics add them, some don't)
    if ".katex {" not in shared and ".katex{" not in shared:
        shared += (
            "\n      .katex { font-size: 1.05em !important; }"
            "\n      .katex-display { overflow-x: auto; padding: 6px 0; }"
        )

    out = f"/* topic-shared-a.css — common styles for Type A topics (01–14)\n"
    out += f"   Generated by optimize_topics.py — do not edit manually */\n\n"
    out += shared + "\n"
    write(TOPICS_DIR / "topic-shared-a.css", out)
    return out


def update_type_a_css(fname, shared_css_lines):
    filepath = TOPICS_DIR / fname
    content = read(filepath)
    before, css, after = extract_style_block(content)
    if css is None:
        print(f"  SKIP {fname}: no <style> found")
        return 0

    root, _ = extract_root_block(css)
    old_lines = css.count("\n")

    # Re-insert only the :root block + link to shared CSS
    link = '    <link rel="stylesheet" href="topic-shared-a.css" />'
    new_style = f"    <style>\n      {root}\n    </style>\n{link}"
    new_content = before + new_style + after
    write(filepath, new_content)
    saved = old_lines - root.count("\n")
    print(f"    saved ~{saved} CSS lines")
    return saved


def process_type_a():
    print("\n=== TYPE A: topics 01-14 (CSS extraction) ===")
    css_out = build_shared_a_css()
    shared_lines = css_out.count("\n")

    total_saved = 0
    for i in range(1, 15):
        fname = f"topic-{i:02d}.html"
        print(f"  {fname}")
        total_saved += update_type_a_css(fname, shared_lines)

    print(f"\n  Total CSS lines saved (Type A): ~{total_saved}")


