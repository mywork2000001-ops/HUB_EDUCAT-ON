"""
Fix KaTeX delimiter config in topic-01 to topic-12.
'\(' in JavaScript string literal evaluates to '(' (backslash lost).
'\\(' evaluates to '\(' which is the correct delimiter for KaTeX.
"""
import os

TOPICS_DIR = (
    r"C:\Users\Administrator\Documents\Claude\Projects"
    r"\платформа для подготовки к блоковому экзамену"
    r"\app\public\lessons\topics"
)

# Bytes: one backslash (0x5c) before each paren/bracket = BROKEN
OLD = bytes([
    # {left:'\(',right:'\)',display:false},{left:'\[',right:'\]',display:true}
    0x7b,0x6c,0x65,0x66,0x74,0x3a,0x27,           # {left:'
    0x5c,0x28,                                       # \(
    0x27,0x2c,0x72,0x69,0x67,0x68,0x74,0x3a,0x27,  # ',right:'
    0x5c,0x29,                                       # \)
    0x27,0x2c,0x64,0x69,0x73,0x70,0x6c,0x61,0x79,0x3a,0x66,0x61,0x6c,0x73,0x65,0x7d,  # ',display:false}
    0x2c,0x7b,0x6c,0x65,0x66,0x74,0x3a,0x27,       # ,{left:'
    0x5c,0x5b,                                       # \[
    0x27,0x2c,0x72,0x69,0x67,0x68,0x74,0x3a,0x27,  # ',right:'
    0x5c,0x5d,                                       # \]
    0x27,0x2c,0x64,0x69,0x73,0x70,0x6c,0x61,0x79,0x3a,0x74,0x72,0x75,0x65,0x7d,  # ',display:true}
])

# Bytes: two backslashes (0x5c 0x5c) before each paren/bracket = CORRECT
NEW = bytes([
    0x7b,0x6c,0x65,0x66,0x74,0x3a,0x27,           # {left:'
    0x5c,0x5c,0x28,                                 # \\(
    0x27,0x2c,0x72,0x69,0x67,0x68,0x74,0x3a,0x27,  # ',right:'
    0x5c,0x5c,0x29,                                 # \\)
    0x27,0x2c,0x64,0x69,0x73,0x70,0x6c,0x61,0x79,0x3a,0x66,0x61,0x6c,0x73,0x65,0x7d,
    0x2c,0x7b,0x6c,0x65,0x66,0x74,0x3a,0x27,
    0x5c,0x5c,0x5b,                                 # \\[
    0x27,0x2c,0x72,0x69,0x67,0x68,0x74,0x3a,0x27,
    0x5c,0x5c,0x5d,                                 # \\]
    0x27,0x2c,0x64,0x69,0x73,0x70,0x6c,0x61,0x79,0x3a,0x74,0x72,0x75,0x65,0x7d,
])

print(f"OLD ({len(OLD)} bytes): ...{OLD[6:12]}...")
print(f"NEW ({len(NEW)} bytes): ...{NEW[6:13]}...")

for n in range(1, 13):
    fname = f"topic-{n:02d}.html"
    fpath = os.path.join(TOPICS_DIR, fname)
    if not os.path.exists(fpath):
        print(f"MISSING: {fname}")
        continue
    with open(fpath, "rb") as f:
        content = f.read()
    if OLD in content:
        new_content = content.replace(OLD, NEW, 1)
        with open(fpath, "wb") as f:
            f.write(new_content)
        # Verify
        has_old = OLD in new_content
        has_new = NEW in new_content
        print(f"FIXED:  {fname}  remaining_old={has_old}  new_present={has_new}")
    elif NEW in content:
        print(f"OK:     {fname}  (already has correct double-backslash delimiter)")
    else:
        print(f"SKIP:   {fname}  (pattern not found in file)")
