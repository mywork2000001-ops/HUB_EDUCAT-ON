def process_lesson(key, data):
    path_rel = data["filename"]
    print(f"  Processing {path_rel}...")
    spec = LessonSpec.from_dict(data)
    path_594728a = path_rel.replace("\\", "/")
    html = LessonBuilder.assemble_from_git(
        BASE, "594728a", f"P002_Math_5_Darslik/{path_594728a}", spec
    )
    if html is None:
        print(f"  [WARN] Could not rebuild from git: {path_rel}")
        return
    out = P002 / path_rel
    LessonBuilder.write(out, html)
    slides = count_slides(spec.theory_html or "")
    print(
        f"  [OK] {out.name:30s} slides={slides} questions={len(data['questions'])}"
    )


